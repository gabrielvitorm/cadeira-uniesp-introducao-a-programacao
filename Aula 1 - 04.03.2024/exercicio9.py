# 9. Solicite ao usuário dois números. Em seguida, mostre o resultado da divisão do primeiro pelo segundo.

print("Nesse programa você deve digitar 2 número diferentes de zero, o primeiro número será dividio pelo segundo:\n")

num1 = int(input("Digite o primeiro número\n"))
num2 = int(input("Digite o segundo número\n"))

div = num1/num2

print(f"A divisão de {num1} pelo {num2}, vai ser igual a {div}\n")